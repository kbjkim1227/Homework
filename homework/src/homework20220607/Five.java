package homework20220607;

public class Five {

	public static void main(String[] args) {
		double a = Math.abs(-3.14);
		System.out.println("abs : " + a);
		
		double b = Math.ceil(5.3);
		System.out.println("ceil : "+ b);
		
		double c = Math.floor(1.2);
		System.out.println("floor : "+ c);
		
		double d = Math.max(12, 13.5);
		System.out.println("max : "+ d);
		
		double e = Math.random();
		System.out.println("random : "+ e);
		
		double f = Math.rint(3.1);
		System.out.println("rint : "+ f);
		
		long g = Math.round(3.6);
		System.out.println("round : "+ g);
	

	}

}
